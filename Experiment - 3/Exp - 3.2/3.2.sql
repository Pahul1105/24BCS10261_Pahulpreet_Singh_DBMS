
SELECT emp_city
FROM EMPLOYEES
GROUP BY emp_city
HAVING  SUM(CASE WHEN EMP_SALARY>=90000 THEN 1 ELSE 0 END)>0


-- FIND THE AVERAGE EMPLOYEE SALARY OF EACH CITY

SELECT emp_city, AVG(EMP_SALARY)::NUMERIC(20,2) AS CNT
FROM EMPLOYEES
GROUP BY emp_city

